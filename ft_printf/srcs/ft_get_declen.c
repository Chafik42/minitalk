#include "ft_printf.h"

int	ft_get_declen(int nb)
{
	long	n;
	int		len;

	len = 0;
	n = nb;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		n = -n;
		len++;
	}
	while (n > 0)
	{
		n = n / 10;
		len++;
	}
	return (len);
}
